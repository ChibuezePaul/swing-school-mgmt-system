    public void dispStudent() {
        int row = tabStudent.getSelectedRow();
        String disp = tabStudent.getModel().getValueAt(row, 0).toString();

        try {
            con = DriverManager.getConnection(db, user, pass);
            pst = con.prepareStatement("SELECT * FROM `student`.`student` WHERE `Matric Number` = '" + disp + "'");
            rs = pst.executeQuery();
            if (rs.next()) {
                imageBytes = rs.getBytes("Passport");
                img1 = getToolkit().createImage(imageBytes);
                labPassport1.setIcon(new ImageIcon(img1.getScaledInstance(labPassport1.getWidth(), labPassport1.getHeight(), Image.SCALE_SMOOTH)));
                txtSpName1.setText(rs.getString("Sponsor's Name"));
                txtSpNumber1.setText(rs.getString("Sponsor's Number"));
                txtSpAddress1.setText(rs.getString("Address"));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Please contact your Admin\n" + ex, "Admin Page", 3);
        }
    }

