    private void btnPaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPaymentActionPerformed
        if (txtPmtName.getText().equals("") || comPmtType.getSelectedItem().equals("Select Payment Type") || comPmtMedium.getSelectedItem().equals("Select Payment Medium") || txtPmtAmt.getText().equals("") || txtPmtID.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Incomplete payment details", "Payment", 3);
        } else {
            try {
                con = DriverManager.getConnection(db, user, pass);
                pst = con.prepareStatement("INSERT INTO `student`.`payment`(`paymentID`,`paymentType`,`paymentBearer`,`paymentMedium`,`tellerID`,`amount`,`paymentDate`)VALUES (?,?,?,?,?,?,?)");

                pst.setString(1, txtPmtID.getText());
                pst.setString(2, comPmtType.getSelectedItem().toString());
                pst.setString(3, txtPmtName.getText());
                pst.setString(4, comPmtMedium.getSelectedItem().toString());
                pst.setString(5, txtTellerID.getText());
                String pmtAmt = String.format("N%.2f", Double.parseDouble(txtPmtAmt.getText()));
                pst.setString(6, pmtAmt);
                pst.setString(7, (String.valueOf(comPmtDate.getSelection().toString())));

                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Payment Successful", "ESM Universite Mgt", JOptionPane.INFORMATION_MESSAGE);
                dispPayment();
                txtTellerID.setText(null);
                txtTellerID.setEnabled(false);
                txtPmtID.setText(null);
                this.panPaymentComponentShown(null);
                comPmtType.setSelectedItem("Select Payment Type");
                txtPmtName.setText(null);
                comPmtMedium.setSelectedItem("Select Payment Medium");
                txtPmtAmt.setText(null);

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Please contact your Admin\n" + ex, "ESM Universite Mgt", 3);
            }
        }
    }//GEN-LAST:event_btnPaymentActionPerformed

