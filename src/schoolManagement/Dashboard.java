    public void uploadPassport() {
        JFileChooser open = new JFileChooser();
        open.showOpenDialog(null);
        File passport = open.getSelectedFile();
        String filename = passport.getAbsolutePath();
        txtPassport.setText(filename);
        ImageIcon img0 = new ImageIcon(passport.toString());
        Image img1 = img0.getImage();
        labPassport.setIcon(new ImageIcon(img1.getScaledInstance(labPassport.getWidth(), labPassport.getHeight(), Image.SCALE_SMOOTH)));

        try {
            File image = new File(filename);
            FileInputStream fis = new FileInputStream(image);
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            byte[] buf = new byte[1024];
            for (int readNum; (readNum = fis.read(buf)) != -1;) {
                bos.write(buf, 0, readNum);
            }

            photo = bos.toByteArray();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Please contact your Admin\n" + ex, "ESM Universite Mgt", 3);
        }
    }
